async function main() {
  var allPlayers = [];
  var teamNames = Object.keys(SQUADS);

  teamNames.forEach(function (team) {
    SQUADS[team].forEach(function (p) {
      allPlayers.push({
        team: team,
        name: p.name,
        position: p.position,
        shirt_number: p.shirt_number,
      });
    });
  });

  console.log("Inserting " + allPlayers.length + " players across " + teamNames.length + " teams...");

  // Insert in batches of 100 to avoid request size limits
  var BATCH = 100;
  var inserted = 0;

  for (var i = 0; i < allPlayers.length; i += BATCH) {
    var batch = allPlayers.slice(i, i + BATCH);
    var res = await supabase
      .from("players")
      .upsert(batch, { onConflict: "team,name" });

    if (res.error) {
      console.error("Error on batch " + (i / BATCH + 1) + ":", res.error.message);
    } else {
      inserted += batch.length;
      process.stdout.write(".");
    }
  }

  console.log("\nDone. Players inserted/updated: " + inserted);
}

main().catch(function (err) {
  console.error("Fatal:", err.message);
  process.exit(1);
});
